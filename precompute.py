def get_location_tier(location_str):
    """
    Returns:
        'preferred'  — Pune or Noida (JD preferred)
        'tier_1'     — Other Tier-1 Indian cities
        'other'      — Everything else
    """
    if not location_str:
        return 'other'
    loc = location_str.lower()
    for city in PREFERRED_CITIES:
        if city in loc:
            return 'preferred'
    for city in TIER_1_CITIES:
        if city in loc:
            return 'tier_1'
    return 'other'

def compute_yoe(career):
    total = 0
    for job in career:
        try:
            s = datetime.strptime(job['start_date'], '%Y-%m-%d')
            e = TODAY if not job.get('end_date') else datetime.strptime(job['end_date'], '%Y-%m-%d')
            total += max(0, (e.year - s.year) * 12 + e.month - s.month)
        except:
            pass
    return round(total / 12, 1)

def get_skill_durations(skills):
    """
    Returns dict of {skill_name_lower: duration_months}
    If duration_months missing, defaults to -1 (unknown)
    -1 = not provided, 0 = listed but never used (keyword stuffer signal)
    """
    result = {}
    for s in skills:
        name = s.get('name', '').lower().strip()
        if name:
            duration = s.get('duration_months', -1)
            if duration is None:
                duration = -1
            result[name] = int(duration)
    return result

