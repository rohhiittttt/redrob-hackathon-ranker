desc_pairs = [[JD_TEXT, t] for t in desc_texts]
raw_desc   = reranker.predict(desc_pairs, batch_size=8, show_progress_bar=True)
print(f'  Raw range: {raw_desc.min():.4f} to {raw_desc.max():.4f}')

# ── Pass 2 — Headline ──────────────────────────────────────────────────────
print('\n[Phase A] Pass 2/3 — Headline...')
headline_pairs = [[JD_TEXT, t] for t in headline_texts]
raw_headline   = reranker.predict(headline_pairs, batch_size=8, show_progress_bar=True)
print(f'  Raw range: {raw_headline.min():.4f} to {raw_headline.max():.4f}')

# Apply tier + seniority multipliers directly on raw headline scores
headline_boosted = []
for i, cid in enumerate(ordered_cids):
    tier_mult, sen_mult = get_headline_multiplier(headline_texts[i])
    headline_boosted.append(float(raw_headline[i]) * tier_mult * sen_mult)
headline_boosted = np.array(headline_boosted)
print(f'  Boosted range: {headline_boosted.min():.4f} to {headline_boosted.max():.4f}')

# ── Pass 3 — Summary ──────────────────────────────────────────────────────
print('\n[Phase A] Pass 3/3 — Summary...')
summary_pairs = [[JD_TEXT, t if t.strip() else 'no summary provided'] for t in summary_texts]
raw_summary   = reranker.predict(summary_pairs, batch_size=8, show_progress_bar=True)
print(f'  Raw range: {raw_summary.min():.4f} to {raw_summary.max():.4f}')

# ── Minmax on career description only before blending ─────────────────────
mm_desc = minmax_final(raw_desc)
print(f'  desc minmax range: {mm_desc.min():.4f} to {mm_desc.max():.4f}')

# ── Blend: minmaxed desc + raw headline boosted + raw summary ─────────────
print('\n[Phase A] Blending scores (desc=0.50, headline=0.30, summary=0.20)...')
W_DESC, W_HEAD, W_SUM = 0.50, 0.30, 0.20
blended = W_DESC * mm_desc + W_HEAD * headline_boosted + W_SUM * raw_summary
print(f'  Blended range: {blended.min():.4f} to {blended.max():.4f}')

# ── Minmax on final blended score only ────────────────────────────────────
normalized_cross = minmax_final(blended)
print(f'  Normalized range: {normalized_cross.min():.4f} to {normalized_cross.max():.4f}')

# ── Distribution stats ─────────────────────────────────────────────────────
all_scores = sorted(normalized_cross.tolist(), reverse=True)
print(f'\n  Top 10 avg  : {np.mean(all_scores[:10]):.4f}')
print(f'  Top 50 avg  : {np.mean(all_scores[:50]):.4f}')
print(f'  Bottom 50   : {np.mean(all_scores[150:]):.4f}')
print(f'  Std dev     : {np.std(all_scores):.4f}')

