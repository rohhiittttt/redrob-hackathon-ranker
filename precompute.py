def get_best_description(career):
    """
    Pick single best description from career history.
    Best = highest tier → tie break by raw text length (more detail = better).
    Returns (description_text, tier_multiplier)
    """
    if not career:
        return '', 0.0

    best_desc = ''
    best_tier = 5  # lower is better
    best_len  = 0

    for role in career:
        desc = role.get('description', '') or ''
        if not desc.strip():
            continue
        tier = detect_desc_tier(desc)
        length = len(desc)

        if tier < best_tier or (tier == best_tier and length > best_len):
            best_tier = tier
            best_desc = desc
            best_len  = length

    tier_mult = TIER_MULTIPLIERS.get(best_tier, 0.0)
    return best_desc.strip(), tier_mult

def chunk_text(text, chunk_size=3):
    """
    Split text into chunks of ~chunk_size sentences.
    Returns list of chunk strings.
    """
    if not text:
        return ['']
    # Split on sentence boundaries
    sentences = re.split(r'(?<=[.!?])\s+', text.strip())
    sentences = [s.strip() for s in sentences if s.strip()]

    if not sentences:
        return [text[:500]]

    chunks = []
    for i in range(0, len(sentences), chunk_size):
        chunk = ' '.join(sentences[i:i + chunk_size])
        if chunk:
            chunks.append(chunk)

    return chunks if chunks else [text[:500]]

